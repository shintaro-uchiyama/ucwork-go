package main

import (
	"fmt"
	"github.com/shintaro123/ucwork-go/internal/endpoint"
	"log"
	"net/http"
	"os"
)

func main() {
	port := os.Getenv("PORT")
	if port == "" {
		port = "8080"
		log.Printf("Defaulting to port %s", port)
	}
	log.Printf("Listening on port %s", port)
	log.Fatal(http.ListenAndServe(fmt.Sprintf(":%s", port), endpoint.RegisterHandlers()))
}
